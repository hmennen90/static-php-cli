<?php

declare(strict_types=1);

namespace SPC\builder\macos\library;

use SPC\builder\ios\SystemUtil as IOSSystemUtil;
use SPC\store\FileSystem;

class icu extends MacOSLibraryBase
{
    use \SPC\builder\unix\library\icu;

    public const NAME = 'icu';

    protected function build(): void
    {
        if (str_starts_with((string) getenv('SPC_TARGET'), 'ios')) {
            $this->buildIOS();
            return;
        }

        $root = BUILD_ROOT_PATH;
        shell()->cd($this->source_dir . '/source')
            ->exec("./runConfigureICU MacOSX --enable-static --disable-shared --disable-extras --disable-samples --disable-tests --prefix={$root}")
            ->exec('make clean')
            ->exec("make -j{$this->builder->concurrency}")
            ->exec('make install');

        $this->patchPkgconfPrefix(patch_option: PKGCONF_PATCH_PREFIX);
        FileSystem::removeDir(BUILD_LIB_PATH . '/icu');
    }

    /**
     * iOS cross-compile.
     *
     * ICU bundles code generators (genrb, pkgdata, ...) that must run on the
     * BUILD machine, so a cross build is two passes:
     *   1. a native macOS "host" build (out-of-tree) just to produce those
     *      tools, and
     *   2. the real iOS build, pointed at the host build via --with-cross-build
     *      so the tools are reused instead of (impossibly) executed as iOS
     *      binaries on the mac.
     *
     * Data is packaged as a static archive (libicudata.a) so it links straight
     * into libphp.a / the app bundle - the iOS sandbox has no writable place to
     * load an external .dat from.
     */
    private function buildIOS(): void
    {
        $root = BUILD_ROOT_PATH;
        $icuSrc = $this->source_dir . '/source';
        $hostDir = $this->source_dir . '/build-host';
        $targetDir = $this->source_dir . '/build-target';
        $concurrency = $this->builder->concurrency;

        $commonConfigure = '--enable-static --disable-shared --disable-extras '
            . '--disable-samples --disable-tests --with-data-packaging=static';

        // ── Pass 1: host tools (native macOS) ──────────────────────────────
        // The process env is primed for iOS (CC/SDKROOT/... point at the
        // iPhoneOS SDK). Override them with native-macOS values so the host
        // tools are real mach-o binaries we can execute. setEnv() skips empty
        // strings, so we point SDKROOT at the macOS SDK rather than unsetting.
        $macSdk = $this->xcrunMac('--show-sdk-path');
        $macCc = $this->xcrunMac('--find clang');
        $macCxx = $this->xcrunMac('--find clang++');
        $macAr = $this->xcrunMac('--find ar');
        $macRanlib = $this->xcrunMac('--find ranlib');
        $nativeEnv = [
            'CC' => $macCc ?: 'clang',
            'CXX' => $macCxx ?: 'clang++',
            'CPP' => ($macCc ?: 'clang') . ' -E',
            'CXXCPP' => ($macCxx ?: 'clang++') . ' -E',
            'AR' => $macAr ?: 'ar',
            'RANLIB' => $macRanlib ?: 'ranlib',
            'SDKROOT' => $macSdk,
            'CFLAGS' => "-isysroot {$macSdk} -O2",
            'CXXFLAGS' => "-isysroot {$macSdk} -O2",
            'LDFLAGS' => "-isysroot {$macSdk}",
        ];

        // Skip the (slow) host build if its tools are already present - lets
        // a failed/retried target build reuse the host pass.
        if (!is_file($hostDir . '/bin/pkgdata')) {
            FileSystem::removeDir($hostDir);
            FileSystem::createDir($hostDir);
            shell()->cd($hostDir)
                ->setEnv($nativeEnv)
                ->exec("{$icuSrc}/runConfigureICU MacOSX {$commonConfigure}")
                ->exec("make -j{$concurrency}");
        }

        // ── Pass 2: iOS cross build ────────────────────────────────────────
        $arch = IOSSystemUtil::getArch();
        $sdkPath = IOSSystemUtil::getSdkPath();
        $iosFlags = IOSSystemUtil::getArchCFlags($arch) . " -isysroot {$sdkPath}";
        $iosEnv = [
            'CFLAGS' => "{$iosFlags} -Os",
            'CXXFLAGS' => "{$iosFlags} -Os",
            'CPPFLAGS' => $iosFlags,
            'LDFLAGS' => $iosFlags,
        ];
        $hostTriple = $arch === 'x86_64' ? 'x86_64-apple-darwin' : 'aarch64-apple-darwin';

        // TOOLS= drops the `tools` subdir from the target build. ICU's
        // generators (pkgdata, genrb, ...) call system()/popen(), which are
        // unavailable on iOS, so they fail to compile - and we never need them:
        // data is generated by the *host* tools via --with-cross-build, and the
        // target only needs the libraries (common/i18n/io) + the packaged data.
        FileSystem::removeDir($targetDir);
        FileSystem::createDir($targetDir);
        shell()->cd($targetDir)
            ->setEnv($iosEnv)
            ->exec(
                "{$icuSrc}/configure --prefix={$root} "
                . "--host={$hostTriple} --with-cross-build={$hostDir} "
                . $commonConfigure
            )
            ->exec("make -j{$concurrency} TOOLS=")
            ->exec('make install TOOLS=');

        $this->patchPkgconfPrefix(patch_option: PKGCONF_PATCH_PREFIX);
        FileSystem::removeDir(BUILD_LIB_PATH . '/icu');
    }

    /** First stdout line of an `xcrun --sdk macosx <args>` call, or '' if none. */
    private function xcrunMac(string $args): string
    {
        $res = shell()->execWithResult("xcrun --sdk macosx {$args}", false);
        return isset($res[1][0]) ? trim((string) $res[1][0]) : '';
    }
}
